public class Convocatoria {
    
    public String NombreConv;
    public String DescConv;
    public String LinkConv;
    
    
    public String getNombreConv() {
        return NombreConv;
    }

    public void setNombreConv(String NombreConv) {
        this.NombreConv = NombreConv;
    }

    public String getDescConv() {
        return DescConv;
    }

    public void setDescConv(String DescConv) {
        this.DescConv = DescConv;
    }

    public String getLinkConv() {
        return LinkConv;
    }

    public void setLinkConv(String LinkConv) {
        this.LinkConv = LinkConv;
    }
    
}
